    <h1>
        about
    </h1>
    <p><a href="<?=CA_URL_ROOT?>">calculatedearth</a> is a collaborative effort by Malcolm Burke and Jonathan Burke and arises out of a piece of background research work that led us to the many freely available topographical data sources on the net.</p>
    <p>The maps and animations are plotted using the <a href="http://www.ngdc.noaa.gov/mgg/fliers/06mgg01.html" target="_blank">June 2006 ETOPO2v2</a> data set from the <a href="http://www.ngdc.noaa.gov" target="_blank">US National Geophysical Data Centre</a>.</p>
    <p>The maps simply show graphically the selected height across the ETOPO2v2 dataset without interpretation or adjustment. So landlocked areas of the world that are below the selected level will show as being flooded regardless of being without a connection to the sea. No adjustments are made for tides.</p>
    <p>Plotting the first few metres of sea level rise should make you think about localised flooding and perhaps global warming, above that, well at the very least it's a good way to visualise the topography of our planet.</p>

